function buildInstincts(
  agentId: string,
  instincts: Array<[label: string, gene: MutationGene, weight: number, state: InstinctState]>,
): AgentInstinct[] {
  return instincts.map(([label, gene, weight, state], index) => ({
    id: `${agentId}-instinct-${index + 1}`,
    label,
    gene,
    weight,
    state,
  }));
}

function buildProvenFrame(frameId: string, result: ProvenFrame['result']): ProvenFrame {
  const frame = getHistoricalChartFrame(frameId);
  if (!frame) {
    return {
      id: frameId,
      title: frameId,
      dateLabel: 'Unknown frame',
      note: 'Frame metadata unavailable.',
      result,
    };
  }
  return {
    id: frame.id,
    title: frame.title,
    dateLabel: frame.dateLabel,
    note: frame.note,
    result,
  };
}

function buildMutationSeed(
  agentId: string,
  title: string,
  gene: MutationGene,
  decision: MutationDecision,
  proofFrameId: string,
  deltaTotal: number,
  reason: string,
  visibleEffect: string,
): AgentMutation {
  return {
    id: `${agentId}-${title.toLowerCase().replaceAll(' ', '-')}`,
    title,
    gene,
    decision,
    reason,
    proofFrameId,
    proofFrameTitle: buildProvenFrame(proofFrameId, decision === 'ACCEPTED' ? 'PASSED' : decision === 'REVERTED' ? 'FAILED' : 'UNSTABLE').title,
    visibleEffect,
    deltaTotal,
    createdAt: Date.UTC(2026, 2, 12),
  };
}

